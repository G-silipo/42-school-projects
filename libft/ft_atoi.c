#include "libft.h"

int		ft_atoi(const char *str)
{
	int 			s;
	int				i;
	unsigned int	r;

	i = 0;
	s = 1;
	r = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if(str[i] == '-')
			s = s * -1;
		i++;
	}
	while(str[i] >= '0' && str[i] <= '9')
	{
		r = (r * 10) + (str[i] - '0');
		if (r > 2147483647)
			break;
		i++;
	}
	if((s == -1 && r == 2147483648) || r <= 2147483647)
		return (r * s);
	return(s < 0 ? -1 : 0);
}
